package com.example.demo.domain;

public class Whisky {
	private Integer id;
	private String name;
	private String brand;
	private String productingArea;
	private String classification;
	private Integer price;
	private Integer alcoholContent;
	private String manufacturer;
	private Integer maturityYears;
	private Integer capacity;
	
	public Integer getId() {
		return id;
	}
	
	public void setId(Integer id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getProductingArea() {
		return productingArea;
	}
	public void setProductingArea(String productingArea) {
		this.productingArea = productingArea;
	}
	public String getClassification() {
		return classification;
	}
	public void setClassification(String classification) {
		this.classification = classification;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public Integer getAlcoholContent() {
		return alcoholContent;
	}
	public void setAlcoholContent(Integer alcoholContent) {
		this.alcoholContent = alcoholContent;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public Integer getMaturityYears() {
		return maturityYears;
	}
	public void setMaturityYears(Integer maturityYears) {
		this.maturityYears = maturityYears;
	}
	public Integer getCapacity() {
		return capacity;
	}
	public void setCapacity(Integer capacity) {
		this.capacity = capacity;
	}
	
}
